uint8_t badge_framebuffer_render_char(badge_framebuffer *fb, int8_t pos_x, int8_t pos_y, char c) {
  if(c == 32) {
    // intentionally left empty.
  } else if(c > 32) {
    badge_sprite sp = { BADGE_FONT_GLYPH_WIDTH, BADGE_FONT_HEIGHT, fontdata[c - 33] };
    badge_framebuffer_blt(fb, pos_x, pos_y, &sp, 0);
  } else {
    return -1;
  }

  return 0;
}

uint8_t badge_framebuffer_render_text(badge_framebuffer *fb, int8_t pos_x, int8_t pos_y, char const *text) {
  uint8_t count = 0;

  while(*text) {
    if(0 != badge_framebuffer_render_char(fb, pos_x, pos_y, *text)) {
      break;
    }

    ++count;
    ++text;
    pos_x += BADGE_FONT_WIDTH;
  }

  return count;
}

static uint8_t badge_framebuffer_render_number_aux(badge_framebuffer *fb, int8_t *pos_x, int8_t pos_y, uint8_t number) {
  if(number >= 10) {
    badge_framebuffer_render_number_aux(fb, pos_x, pos_y, number / 10);
  }

  uint8_t err = badge_framebuffer_render_char(fb, *pos_x, pos_y, '0' + number % 10);

  if(err != 0) return err;

  *pos_x += BADGE_FONT_WIDTH;
  return 0;
}

uint8_t badge_framebuffer_render_number(badge_framebuffer *fb, int8_t pos_x, int8_t pos_y, uint8_t number) {
  return badge_framebuffer_render_number_aux(fb, &pos_x, pos_y, number);
}
